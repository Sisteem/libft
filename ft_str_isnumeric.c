#include "libft.h"

int	ft_str_isnumeric(char *s)
{	
	int	i;

	i = 0;
	while (s[i] >= '0' && s[i] <= '9')
	{
		++i;
		if (s[i] == '\0')
			return (1);
	}
	return (0);
}
